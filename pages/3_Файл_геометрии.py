# Страница: Файл геометрии

import streamlit as st

def show():
    st.title("🗂️ Файл геометрии Gmsh (.geo)")
    
    st.markdown("""
    ## Что такое `.geo` файл?
    
    Это текстовый файл на собственном языке программирования Gmsh, который служит для 
    **параметрического описания геометрической модели**.
    """)
    
    with st.expander("🔍 Подробнее о структуре файла", expanded=True):
        col1, col2 = st.columns(2)
        with col1:
            st.markdown("""
            ### Синтаксис
            """)
            st.code("""
// Комментарии как в C++
/* Многострочные комментарии */

// Переменные
lc = 0.1;
radius = 5.0;

// Команды
Point(1) = {0, 0, 0, lc};
Line(1) = {1, 2};
            """, language="python")
        
        with col2:
            st.markdown("""
            ### Типы данных
            
            - **Числа** (все с плавающей точкой): `1.5`, `2e-3`
            - **Строки**: `"filename.step"`
            - **Списки**: `{1, 2, 3, 4}`
            - **Диапазоны**: `{1:10}` (от 1 до 10)
            """)
    
    st.markdown("---")
    
    st.markdown("""
    ### 📌 Основные разделы файла
    
    Файл `.geo` может содержать:
    
    1. **Определения переменных** — для параметризации
    2. **Геометрические примитивы** — точки, линии, поверхности
    3. **Трансформации** — перенос, вращение, масштабирование
    4. **Физические группы** — для маркировки
    5. **Опции сетки** — настройки meshing
    """)
    
    st.code("""
// Пример полноценного .geo файла
lc = 0.1;                          // параметр

Point(1) = {0, 0, 0, lc};          // точка с размером сетки
Point(2) = {1, 0, 0, lc};
Point(3) = {1, 1, 0, lc};
Point(4) = {0, 1, 0, lc};

Line(1) = {1, 2};                  // линии
Line(2) = {2, 3};
Line(3) = {3, 4};
Line(4) = {4, 1};

Curve Loop(1) = {1, 2, 3, 4};      // замкнутый контур
Plane Surface(1) = {1};             // поверхность

Physical Surface("MySurface", 1) = {1};  // физическая группа
    """, language="python")

# Для автономного тестирования
if __name__ == "__main__":
    show()
