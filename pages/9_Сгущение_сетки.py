st.set_page_config(
    page_title="Сгущение сетки в Gmsh",
    page_icon="🔬",
    layout="wide"
)


# Создание вкладок для каждого слайда
tabs = st.tabs([
    "Сгущение сетки",
    "Практические методы"
])

