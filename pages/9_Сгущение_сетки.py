# ==================== СЛАЙД 2 ====================
with tabs[1]:
    st.header("🛠️ Практические методы реализации локального сгущения")
    
    st.markdown("""
    GMSH предоставляет **два основных интерфейса** для управления сеточной декомпозицией:
    """)
    
    col1, col2 = st.columns(2)
    
    with col1:
        st.subheader("🖥️ Графический интерфейс (GUI)")
        st.markdown("""
        - Визуальное управление параметрами
        - Интерактивная настройка полей
        - Удобно для быстрого прототипирования
        """)
    
    with col2:
        st.subheader("💻 Программный интерфейс")
        st.markdown("""
        - Скрипты на собственном языке **.geo**
        - API на **Python**
        - Автоматизация и воспроизводимость
        """)
    
    st.divider()
    
    st.subheader("📍 Метод 1: Поле размера на основе расстояния до аттракторов")
    
    st.markdown("""
    Этот подход позволяет **автоматически создавать сгущение** вблизи выбранных геометрических объектов 
    без необходимости ручного задания размеров для каждого элемента.
    """)
    
    st.code("""
// Пример в скрипте .geo
Field[1] = Distance;
Field[1].NodesList = {1, 2, 3};  // Точки-аттракторы
Field[1].NNodesByCell = 100;

Field[2] = Threshold;
Field[2].IField = 1;
Field[2].LcMin = 0.01;  // Минимальный размер
Field[2].LcMax = 0.5;   // Максимальный размер
Field[2].DistMin = 0.1;
Field[2].DistMax = 1.0;

Background Field = 2;
    """, language="cpp")
    
    st.divider()
    
    st.subheader("🔷 Метод 2: Использование метрических тензоров")
    
    st.markdown("""
    Этот подход позволяет задавать не только **размер ячейки**, но и её **форму и ориентацию**, 
    что необходимо для **анизотропного сгущения**.
    """)
    
    st.code("""
// Пример создания метрического поля в скрипте .geo
Field[1] = MathEval;
Field[1].F = "0.01 + 0.1*x^2 + 0.05*y^2";  // Аналитическая функция

Field[2] = Box;
Field[2].VIn = 0.01;   // Размер внутри области
Field[2].VOut = 0.5;   // Размер снаружи
Field[2].XMin = 0;
Field[2].XMax = 1;
Field[2].YMin = 0;
Field[2].YMax = 1;
Field[2].ZMin = 0;
Field[2].ZMax = 1;

Background Field = 2;
    """, language="cpp")
    
    st.warning("⚠️ Для применения поля необходимо установить его как Background Field")
    
    st.divider()
    
    st.subheader("📊 Сравнение методов")
    
    st.markdown("""
    | Метод | Преимущества | Недостатки |
    |-------|-------------|------------|
    | **Distance Field** | Автоматическое сгущение у границ | Требует задания аттракторов |
    | **Threshold Field** | Плавный переход размеров | Нужно подбирать параметры |
    | **MathEval Field** | Гибкость, аналитический контроль | Сложнее для сложных геометрий |
    | **Box Field** | Простота для прямоугольных областей | Ограниченная геометрия |
    | **Метрические тензоры** | Анизотропное сгущение, контроль ориентации | Высокая сложность настройки |
    """)
    
    st.success("✅ Выбор метода зависит от конкретной задачи и требуемой точности расчёта")

